import UIKit.UIColor

extension UIColor {
    
    // rgba(41, 128, 185,1.0)
    
    static var listening: UIColor {
        return UIColor(red: 41 / 255,
                       green: 128 / 255,
                       blue: 185 / 255,
                       alpha: 1.0)
    }
    
    // rgba(231, 76, 60,1.0)
    
    static var error: UIColor {
        return UIColor(red: 231 / 255,
                       green: 76 / 255,
                       blue: 60 / 255,
                       alpha: 1.0)
    }
    
}
